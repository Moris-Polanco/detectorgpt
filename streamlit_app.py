import openai
import streamlit as st
import os

# Autenticaci√≥n de OpenAI (oculta la clave en una variable de entorno)
openai.api_key = os.environ.get("OPENAI_API_KEY")

def detect_gpt3(prompt):
    completions = openai.Completion.create(
        engine="text-davinci-003",
        prompt=prompt,
        max_tokens=2048,
        n=1,
        stop=None,
        temperature=0.5,
    )

    message = completions.choices[0].text
    return message

st.title("GPT-3 Text Detection App")

text = st.text_area("Enter text to check if generated by GPT-3:")

if st.button("Check"):
    result = detect_gpt3(text)
    if "gpt3" in result.lower():
        st.write("The text is generated by GPT-3")
    else:
        st.write("The text is not generated by GPT-3")
